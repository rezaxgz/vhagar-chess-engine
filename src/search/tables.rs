pub fn get_sort_tabel_value(piece: Piece, square: Square, color: Color) -> i16 {
    if piece == Piece::King || piece == Piece::Pawn {
        return 0;
    }
    return SORT_PIECE_SQUARE_TABLES[color as usize][piece as usize][square as usize];
}
